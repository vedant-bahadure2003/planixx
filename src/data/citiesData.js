// Get all cities combined (for search)
export const getAllCities = () => {
  const popular = popularCities.map(c => ({ ...c, isPopular: true }));
  const others = otherCities.map(name => ({
    id: name.toLowerCase().replace(/\s+/g, '-'),
    name,
    icon: 'ðŸ“',
    color: 'from-gray-500 to-gray-600',
    isPopular: false
  }));
  return [...popular, ...others];
};

// Search cities
export const searchCities = (query) => {
  if (!query.trim()) return { popular: popularCities, others: otherCities };
  
  const lowerQuery = query.toLowerCase();
  const filteredPopular = popularCities.filter(c => 
    c.name.toLowerCase().includes(lowerQuery)
  );
  const filteredOthers = otherCities.filter(c => 
    c.toLowerCase().includes(lowerQuery)
  );
  
  return { popular: filteredPopular, others: filteredOthers };
};

export default { popularCities, otherCities, getAllCities, searchCities };
